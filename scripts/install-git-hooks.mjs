#!/usr/bin/env node

/**
 * install-git-hooks.mjs
 *
 * Installs git hooks for this repository.
 *
 * Hooks installed:
 *   pre-push — runs `npm run docs:generate` and `node scripts/update-steering.mjs`
 *              to keep generated docs in sync before every push.
 *
 * Usage:
 *   node scripts/install-git-hooks.mjs
 *   npm run hooks:install
 */

import { fileURLToPath } from 'url';
import path from 'path';
import fs from 'fs';
import { execSync } from 'child_process';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, '..');

function findGitRoot(from) {
  let dir = from;
  while (dir !== path.dirname(dir)) {
    if (fs.existsSync(path.join(dir, '.git'))) return dir;
    dir = path.dirname(dir);
  }
  return null;
}

const gitRoot = findGitRoot(ROOT);
if (!gitRoot) {
  console.error('Could not find .git directory. Run this from inside a git repository.');
  process.exit(1);
}

const hooksDir = path.join(gitRoot, '.git', 'hooks');
fs.mkdirSync(hooksDir, { recursive: true });

// ---------------------------------------------------------------------------
// pre-commit hook
// ---------------------------------------------------------------------------

const preCommitPath = path.join(hooksDir, 'pre-commit');

// Resolve the package.json dir relative to git root so the hook works
// whether the backend is a standalone repo or nested inside a monorepo.
const pkgDir = ROOT;

const preCommitScript = `#!/bin/sh
# pre-commit hook — auto-generated by scripts/install-git-hooks.mjs
# Syncs DMMS database, regenerates docs, and updates steering before every commit.
# Generated files are auto-staged so they are included in the commit.
set -e

REPO_ROOT="${pkgDir}"

echo "[pre-commit] Syncing docs..."
cd "$REPO_ROOT"
npm run docs:generate --silent
node scripts/update-steering.mjs

# Auto-stage all generated files so they are part of this commit
git add docs/status/ docs/.vitepress/data/ .claude/steering/structure.md 2>/dev/null || true

echo "[pre-commit] Done."
`;

fs.writeFileSync(preCommitPath, preCommitScript, { mode: 0o755 });
console.log(`  Installed ${path.relative(gitRoot, preCommitPath)}`);

// ---------------------------------------------------------------------------
// Report
// ---------------------------------------------------------------------------

console.log('\nGit hooks installed:');
console.log('  pre-commit → npm run docs:generate + update-steering.mjs + git add generated files');
console.log('\nTo uninstall: rm .git/hooks/pre-commit');
