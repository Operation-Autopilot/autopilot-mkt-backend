#!/usr/bin/env node

/**
 * install-git-hooks.mjs
 *
 * Installs git hooks for this repository.
 *
 * Hooks installed:
 *   pre-push — runs `npm run docs:generate` and `node scripts/update-steering.mjs`
 *              to keep generated docs in sync before every push.
 *
 * Usage:
 *   node scripts/install-git-hooks.mjs
 *   npm run hooks:install
 */

import { fileURLToPath } from 'url';
import path from 'path';
import fs from 'fs';
import { execSync } from 'child_process';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, '..');

function findGitRoot(from) {
  let dir = from;
  while (dir !== path.dirname(dir)) {
    if (fs.existsSync(path.join(dir, '.git'))) return dir;
    dir = path.dirname(dir);
  }
  return null;
}

const gitRoot = findGitRoot(ROOT);
if (!gitRoot) {
  console.error('Could not find .git directory. Run this from inside a git repository.');
  process.exit(1);
}

const hooksDir = path.join(gitRoot, '.git', 'hooks');
fs.mkdirSync(hooksDir, { recursive: true });

// ---------------------------------------------------------------------------
// pre-push hook
// ---------------------------------------------------------------------------

const prePushPath = path.join(hooksDir, 'pre-push');

// Resolve the package.json dir relative to git root so the hook works
// whether the backend is a standalone repo or nested inside a monorepo.
const pkgDir = ROOT;

const prePushScript = `#!/bin/sh
# pre-push hook — auto-generated by scripts/install-git-hooks.mjs
# Regenerates DMMS docs and steering structure before push.
set -e

REPO_ROOT="${pkgDir}"

echo "[pre-push] Syncing docs..."
cd "$REPO_ROOT"
npm run docs:generate --silent
node scripts/update-steering.mjs

# Stage any modified generated files so they are included if the user
# is using \`git commit --amend\` or creating a docs-only commit.
# We don't auto-commit here — just regenerate so the working tree is clean.
echo "[pre-push] Done."
`;

fs.writeFileSync(prePushPath, prePushScript, { mode: 0o755 });
console.log(`  Installed ${path.relative(gitRoot, prePushPath)}`);

// ---------------------------------------------------------------------------
// Report
// ---------------------------------------------------------------------------

console.log('\nGit hooks installed:');
console.log('  pre-push → npm run docs:generate + update-steering.mjs');
console.log('\nTo uninstall: rm .git/hooks/pre-push');
